// 덧셈
let sum = 10 + 20;
console.log(sum); // 30

// 뺄셈
let difference = 30 - 10;
console.log(difference); // 20

// 곱셈
let product = 5 * 6;
console.log(product); // 30

// 나눗셈
let quotient = 21 / 4;
console.log(quotient); // 5

// 나머지
let remainder = 10 % 3;
console.log(remainder); // 1

// 거듭제곱
let power = 2 ** 3;
console.log(power); // 8 (2의 3승)
