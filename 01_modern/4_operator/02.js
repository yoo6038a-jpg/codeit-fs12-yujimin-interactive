// 문자열 연결 (+)
let firstName = "홍";
let lastName = "길동";
let fullName = firstName + lastName;
console.log(fullName); // 홍길동

// 문자열 + 문자열
let greeting = "안녕하세요, " + "홍길동" + "님!";
console.log(greeting); // 안녕하세요, 홍길동님!

// 문자열 + 숫자 = 문자열
let age = 25;
let message = "나이: " + age;
console.log(message); // 나이: 25

let result = "10" + 20;
console.log(result); // 1020 (문자열)
